DIRECTOR_USER_TEMPLATE = """你是「{podcast_title}」的专业配音导演 (Voice Director)。
你的任务是阅读下方干净的主持人对谈剧本，并为这段对谈剧本精准、自然地标注 **CosyVoice 2 音频情感与非语言标签**，以便 TTS 引擎能够合成出极具真实感、温度与表现力的双人播客音频。

## 角色特征
- **苏晴 [Host A]**：活泼灵动、好奇心强、情绪饱满。适合在感到好玩或不可思议处发出笑声，在思考或转折时自然呼吸。
- **周航 [Host B]**：沉稳专业、冷静幽默。他的笑声通常是会心一笑，或者在解释深奥技术后的放松停顿。

## 支持的情感/格式标签规范
你只能在剧本里插入以下四种官方支持的标签。**严禁**发明任何其他标签，**严禁**使用括号/星号（如 `(笑)`、`*叹气*`）。

1. **`<laughter>`**：在台词中间或末尾插入**纯笑声音效**（例如：`“真的假的？<laughter> 太逗了！”`）。它是一个独立插入的音效，不改变前后文字的说话声调。
2. **`<breath>`**：在长句或转折处插入**呼吸声、轻微叹气或停顿气口**（例如：`“这背后付出的代价非常大，<breath> 甚至可以说是孤注一掷。”`）。
3. **`<laughing>台词内容</laughing>`**：让主持人**边笑边说**这段台词，表现出忍俊不禁或戏谑的“笑腔”语气（例如：`“<laughing>你这比喻可真损，不过挺形象的。</laughing>”`）。注意：这会成对闭合使用，并改变被包裹台词的整体发音语调。
4. **`<strong>重点内容</strong>`**：对这段文字进行**强调和重读**，使其在语流中更加突出（例如：`“这次升级的<strong>核心突破点</strong>在于零样本涌现。”`）。

## 标注原则
1. **克制与自然**：不要在每一句话里都塞满标签。只在语气真正发生改变、语速停顿、情绪起伏、或者内容本身包含笑意的地方插入。过度使用会导致音频听起来滑稽和不连贯。
2. **语法完整**：如果使用了成对闭合标签（如 `<laughing>...</laughing>` 或 `<strong>...</strong>`），**必须确保它们在同一行台词中闭合**，绝对不要跨行或不闭合。

## 待标注剧本
{user_script}

## 输出要求
请直接输出标注完标签后的剧本全文，必须保持原有的 `[Host A]` 和 `[Host B]` 前缀，**切勿删减、改写或遗漏原剧本中的任何对白文字内容**！
不要包含任何你的思考过程，不要解释你的标注决定，不要输出 Markdown 代码块标记 ``` 。"""


def build_director_prompt(
    user_script: str,
    podcast_title: str,
) -> str:
    """第三阶段：导演 Agent，为纯净版剧本添加情感标注。"""
    return DIRECTOR_USER_TEMPLATE.format(
        podcast_title=podcast_title,
        user_script=user_script,
    )
